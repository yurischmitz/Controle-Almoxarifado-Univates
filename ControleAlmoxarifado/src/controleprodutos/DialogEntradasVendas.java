    private void btnProcessarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcessarActionPerformed
        // TODO add your handling code here:
        int linha = tabelaDados.getSelectedRow();
        if ( (validarCampos() == true) && (linha >= 0) ) {
        String sql = "";
        if(rbEntrada.isSelected()){
            sql = "UPDATE produtos SET quantidade=quantidade+? WHERE id=?";
        } else {
            sql = "UPDATE produtos SET quantidade=quantidade-? WHERE id=?";
        }
        try {
            PreparedStatement ps = con.prepareStatement(sql);
            ps.setString(1, (txtQuantidade.getText()));
            ps.setString(2, tabelaDados.getValueAt(linha, 0).toString());
            ps.execute();
            ps.close();
            mostrarDados();
            txtQuantidade.setText("");
            
            JOptionPane.showMessageDialog(null, "Estoque alterado!");
        } catch (Exception e) {
            e.printStackTrace();
        }
        }    
    }//GEN-LAST:event_btnProcessarActionPerformed

