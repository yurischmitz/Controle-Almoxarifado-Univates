    private boolean validarCampos() {
        if ((txtQuantidade.getText().trim().length() == 0)) {
           return false;
        } try{
             int quantidade = Integer.parseInt(txtQuantidade.getText());
        } catch (Exception e) {
            return false;
        }
        return true;
    }
    private void mostrarDados() {
        
        DialogProdutos dp = new DialogProdutos(null, true);
        
        DecimalFormat df = new DecimalFormat("0.00");
        try {
            PreparedStatement ps = con.prepareStatement("SELECT * FROM produtos");
            ResultSet rs = ps.executeQuery();
            DefaultTableModel model = (DefaultTableModel) tabelaDados.getModel();
            model.setRowCount(0); // Limpar Tabela
            while (rs.next()) {
                Object linha[] = {rs.getString("id"), rs.getString("nome"), 
                                  dp.getNomeMarca(rs.getInt("marca")), rs.getString("vendedor"),
                                  rs.getString("quantidade")};
                model.addRow(linha); //Adicionar linha
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

